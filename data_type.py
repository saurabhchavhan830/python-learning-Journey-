#  Python has several built-in data types. They define the type of value a variable holds.

x = 20  # int (integer) Whole numbers (positive or negative)

y = 20.5  # float (Numbers with decimal)

name = "saurabh"  # str (string) Sequence of character

z = True # bool (boolean) Logical values [True or False]

